
int Function (int) somaParcial(int a) {

  int b = 20;
  int c = 30;


  // Essa função se lembra do que aconteceu no escopo pai
  return (int d) {
    return a + b + c + d;
  };
}

main() {

  var valor_A = somaParcial(10);

  
  // quando eu chamo o valor_A() com um novo parametro eu estou me lembrando do primeiro valor atribuido e depois chamo o parametro D da segunda função
  print(valor_A(40));
}